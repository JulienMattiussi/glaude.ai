function pts(points: Point[], drift = 0): string {
  return points.map(([x, y]) => `${x + drift * (y / 100)},${y}`).join(" ");
}

interface Props {
  active: boolean;
  boltCount: number;
}

export function LightningEffect({ active, boltCount }: Props) {
  const bolts = BOLTS.slice(0, Math.min(boltCount, BOLTS.length));
  // Random tilt per bolt: ±18 units at y=100 ≈ ±10°
  // Recomputed only when active toggles (new trigger), stable during animation
  const drifts = useMemo(
    () => bolts.map(() => (Math.random() * 2 - 1) * 18),
    // eslint-disable-next-line react-hooks/exhaustive-deps
    [active]
  );

  if (!active) return null;

  return (
    <div key={Date.now()} className="fixed inset-0 pointer-events-none z-9999">
      <div
        className="absolute inset-0"
        style={{
          background: "white",
          animation: "lightning-screen-flash-anim 1.1s ease-out forwards",
        }}
      />

      {bolts.map((bolt, i) => (
        <div
          key={i}
          className="absolute inset-0"
          style={{
            animation: `lightning-bolt-anim 1.1s ease-out ${i * 90}ms forwards`,
          }}
        >
          <svg
            width="100%"
            height="100%"
            viewBox="0 0 100 100"
            preserveAspectRatio="none"
            xmlns="http://www.w3.org/2000/svg"
          >
            <defs>
              <filter id={`glow-${i}`} x="-150%" y="-5%" width="400%" height="110%">
                <feGaussianBlur stdDeviation="1.2" result="blur" />
                <feMerge>
                  <feMergeNode in="blur" />
                  <feMergeNode in="SourceGraphic" />
                </feMerge>
              </filter>
              <filter id={`tglow-${i}`} x="-150%" y="-5%" width="400%" height="110%">
                <feGaussianBlur stdDeviation="0.7" result="blur" />
                <feMerge>
                  <feMergeNode in="blur" />
                  <feMergeNode in="SourceGraphic" />
                </feMerge>
              </filter>
            </defs>

            {/* Main bolt */}
            <polyline
              points={pts(bolt.main, drifts[i])}
              stroke="rgba(180,220,255,0.7)"
              strokeWidth="0.6"
              fill="none"
              strokeLinecap="round"
              strokeLinejoin="round"
              filter={`url(#glow-${i})`}
            />
            <polyline
              points={pts(bolt.main, drifts[i])}
              stroke="rgba(230,245,255,0.98)"
              strokeWidth="0.25"
              fill="none"
              strokeLinecap="round"
              strokeLinejoin="round"
            />
            <polyline
              points={pts(bolt.main, drifts[i])}
              stroke="white"
              strokeWidth="0.18"
              fill="none"
              strokeLinecap="round"
              strokeLinejoin="round"
            />

            {/* Branches */}
            {bolt.branches.map((branch, j) => (
              <g key={j}>
                <polyline
                  points={pts(branch, drifts[i])}
                  stroke="rgba(160,210,255,0.45)"
                  strokeWidth="0.3"
                  fill="none"
                  strokeLinecap="round"
                  strokeLinejoin="round"
                  filter={`url(#tglow-${i})`}
                />
                <polyline
                  points={pts(branch, drifts[i])}
                  stroke="rgba(220,240,255,0.8)"
                  strokeWidth="0.2"
                  fill="none"
                  strokeLinecap="round"
                  strokeLinejoin="round"
                />
              </g>
            ))}

            {/* Twigs (sub-branches) */}
            {bolt.twigs.map((twig, j) => (
              <g key={j}>
                <polyline
                  points={pts(twig, drifts[i])}
                  stroke="rgba(160,210,255,0.35)"
                  strokeWidth="0.45"
                  fill="none"
                  strokeLinecap="round"
                  strokeLinejoin="round"
                  filter={`url(#tglow-${i})`}
                />
                <polyline
                  points={pts(twig, drifts[i])}
                  stroke="rgba(210,235,255,0.55)"
                  strokeWidth="0.12"
                  fill="none"
                  strokeLinecap="round"
                  strokeLinejoin="round"
                />
              </g>
            ))}
          </svg>
        </div>
      ))}
    </div>
  );
}
